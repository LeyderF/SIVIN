    private void registrarUsuario() {
    String nombre = txt_nombres.getText().trim();
    String apellido = txt_apellidos.getText().trim();
    String usuario = txt_usuario.getText().trim();
    String telefono = txt_telefono.getText().trim();
    String password = txt_password.getText().trim();

    if (!nombre.isEmpty() && !apellido.isEmpty() && !usuario.isEmpty()
            && !telefono.isEmpty() && !password.isEmpty()) {

        Usuario nuevoUsuario = new Usuario();
        nuevoUsuario.setNombre(nombre);
        nuevoUsuario.setApellido(apellido);
        nuevoUsuario.setUsuario(usuario);
        nuevoUsuario.setTelefono(telefono);
        nuevoUsuario.setPassword(password);
        nuevoUsuario.setEstado(1); // activo

        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
        if (controlUsuario.registrar(nuevoUsuario)) {
            JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente");
            this.dispose(); // o limpia los campos
            // new FrmLogin().setVisible(true); // si tienes pantalla de login
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar usuario");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
    }
}


